# THE BUG (Alaric, playtest 2026-07-11: "the stones in tunnels ... today they're just suppressed").
# The client's vanilla suppressor is keyed on the ITEM ID, not on where the item came from: detour.rs
# only ever sees `raw_id` off the AddItemFunc buffer. checkItemFlags maps {vanilla FullID -> the check
# flags that ware backs}, and should_suppress() eats the bag-add until EVERY one of those flags is
# collected. So an item that merely HAPPENS to be some check's vanilla ware is eaten from EVERY source:
#
#     Golden Rune [1] is the ware of 46 checks -> every Golden Rune [1] you pick up ANYWHERE is
#     suppressed until all 46 are collected. Same for Stonesword Key (32), Rune Arc (25).
#
# Mine an ore node in a tunnel -> you get a Smithing Stone -> that stone is some check's ware -> eaten.
# Farming, mining and crafting of ~689 item types are dead for most of a run. It also silently ate 83%
# of the rerolled enemy drops (2346 / 2812 slots), which is how we found it.
#
# THE FIX. Suppression by id is only SOUND for a ware you can obtain ONLY as that check. If the same
# item is also farmable / mineable / buyable / craftable, suppressing it by id kills a legitimate source;
# NOT suppressing it merely means you get the vanilla ware alongside the AP item at that one check -- a
# double-dip on an item you could have farmed anyway. Strictly the lesser evil. So derive the set of
# goods obtainable from a REPEATABLE (non-check) source and let check_item_flags exclude them:
#
#     repeatable = goods in any UNFLAGGED ItemLotParam lot (map or enemy)   [flagged == a check]
#                | goods sold by any shop row
#                | goods produced by any crafting recipe
#
# Splits 689 suppressed ids into 243 "also repeatable" (the bug) + 446 "check-only" (safe to suppress).
#
# NOTE the better fix, deliberately NOT taken here: now that we can write ItemLotParam at runtime
# (enemy_drops.rs proves it), we could rewrite each CHECK lot to a SYNTHETIC goods id -- the old baker's
# trick, done live -- making check pickups unambiguous and letting the id heuristic be deleted outright,
# with no double-dip at all. It is blocked on goods-row SUPPLY (goods_row_fields decodes the AP location
# out of unused fields of an EXISTING EquipParamGoods row, so it needs ~1 spare row per check, and we can
# write rows at runtime but not ADD them). Spec'd in SPEC-runtime-minibake-20260711.md.
