def _load_derived_dungeon_regions():
    """map_id -> region from greenfield/dungeon_regions.tsv (grace join + MSB ConnectCollision)."""
    _p = os.path.join(HERE, "dungeon_regions.tsv")
    _out = {}
    if not os.path.isfile(_p):
        print("[gen_data] WARNING: dungeon_regions.tsv absent -- only the curated rows apply")
        return _out
    with open(_p, encoding="utf-8", newline="") as _fh:
        for _r in csv.DictReader(_fh, delimiter="\t"):
            _mid = _r.get("map_id")
            if _mid and _r.get("region"):
                _out[_mid + "_00_00"] = _r["region"]       # full 4-part key, as region_of expects
    return _out

DUNGEON_REGION_OVERRIDE = _load_derived_dungeon_regions()
# A curated row the derivation now AGREES with is redundant -- and a redundant manual override is
# a failure, not belt-and-braces (see the _BOSS_DROP_EXTRAS guard above). Hard error.
_ddr_redundant = sorted(k for k, v in DUNGEON_REGION_CURATED.items()
                        if DUNGEON_REGION_OVERRIDE.get(k) == v)
if _ddr_redundant:
    raise SystemExit("gen_data: REDUNDANT DUNGEON_REGION_CURATED entr%s %r -- dungeon_regions.tsv "
                     "now derives the same region. Delete the override(s)."
                     % ("y" if len(_ddr_redundant) == 1 else "ies", _ddr_redundant))
DUNGEON_REGION_OVERRIDE.update(DUNGEON_REGION_CURATED)     # curation always wins over the derivation
print(f"dungeon regions: {len(DUNGEON_REGION_OVERRIDE)} maps "
      f"({len(DUNGEON_REGION_OVERRIDE) - len(DUNGEON_REGION_CURATED)} derived + "
      f"{len(DUNGEON_REGION_CURATED)} curated)")

# Map recovery for UNPLACED dungeon checks: a catacomb/cave/tunnel pickup identified only by flag
# prefix (method 'flag_prefix', map PENDING) still encodes its map in the flag (30.XX.. -> m30_XX), so
# recover map = mAA_BB_00_00 -- IF that map is a known dungeon (in DUNGEON_REGION_OVERRIDE). Without
# this the check has no map, so region_of hits the coarse Catacombs->Limgrave bucket AND the map-local
# boss sweep for that dungeon can't grant it (9 catacombs were left with empty sweeps). region_of then
# regions it via DUNGEON_REGION_OVERRIDE (the recovered map id), and Phase 3b sweeps it map-locally.
for _rr in _ALLROWS:
    if _rr.get("method") != "flag_prefix" or (_rr.get("map") or "") not in ("", "PENDING"):
        continue
    _fs = str(_rr.get("flag") or "")
    # "11"/"12"/"20"/"21"/"35" added 2026-07-12 (region-spine v2): the m11 capital / m12
    # underground / m20 Belurat+Enir-Ilim / m21 Shadow Keep / m35 Shunning-Grounds flag_prefix rows
    # used to ride multi-region catch-all LABELS ('Leyndell / Roundtable / Shunning-Grounds',
    # 'Eternal Cities & Underground Rivers', 'DLC Interior', 'Divine Tower' -- whose rows are
    # actually m35!, ...). Recovering their self-encoded map lets dungeon_regions.tsv (the grace
    # join) file each check in its REAL region -- m11_10 rows land in the HUB, m12_05 in Mohgwyn.
    # ORDER MATTERS. The DLC-overworld form is 10 digits and ALSO starts "20", so it matched the
    # interior branch first (decoding 2049490900 -> "m20_49_00_00", not a dungeon, silently nothing)
    # and the elif below never ran. Interior flags are 8 digits; DLC-overworld are 10. Split on that.
    if len(_fs) == 8 and _fs[:2] in ("11", "12", "20", "21", "30", "31", "32", "34", "35",
                                     "39", "40", "41", "42", "43"):
        _rec = f"m{_fs[:2]}_{_fs[2:4]}_00_00"
        if _rec in DUNGEON_REGION_OVERRIDE:
            _rr["map"] = _rec
    # DLC OVERWORLD flag_prefix rows (2026-07-13). A 10-digit `20AABBLLLL` flag self-encodes the m61
    # tile, exactly like the interior prefixes above -- but nothing decoded it, so all 234 of these fell
    # through to the region COLUMN, which for every one of them reads 'Land of Shadow (DLC)'. That is
    # not a place, it is a placeholder (same class as 'Global / Filler'), and REGION_MAP dumped 212 of
    # them into Gravesite -- including Commander Gaius's drop (2049490900 -> m61_49_49), while the
    # Scadutree Fragments on the SAME TILE resolved correctly to Scaduview via the global-recovery tile
    # path. Same tile, two answers: the tell that a placeholder was being mapped instead of decoded.
    # Decode the tile and let the m61 grace-anchor NN region it, like every other overworld row.
    elif len(_fs) == 10 and _fs[:2] == "20":
        _rr["map"] = f"m61_{_fs[2:4]}_{_fs[4:6]}_00"


