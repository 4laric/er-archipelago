# ---- CHECK LOTS: blank the vanilla ware AT THE SOURCE (SPEC-runtime-minibake) -----------------------
# The id-keyed suppressor is a workaround for a question detour.rs cannot answer: "where did this item
# come from?" It only sees `raw_id` off the AddItemFunc buffer. Answer the question at the SOURCE
# instead: rewrite the CHECK's own item lot so it never hands out the vanilla ware in the first place.
#
# We can write ItemLotParam at runtime (enemy_drops.rs proves it). And -- the unlock -- we do NOT need a
# synthetic id per check, which is what killed the original spec (3069 colliding checks vs 332 spare
# goods rows). Checks are DETECTED BY THE FLAG POLL (core.rs:1299 pushes the location when its
# acquisition flag fires), not by the item id. The synthetic-per-location scheme was a baker-era relic.
#
# So ONE placeholder goods row is enough: point every check lot's GOODS slot at it, suppress that single
# id unconditionally in the detour, and let the flag poll report the check. Then:
#   * no vanilla ware is EVER handed out at a check -> the double-dip the REPEATABLE_GOODS stopgap
#     accepted is gone;
#   * nothing else is watched by item id -> mined ore, farmed drops, bought and crafted goods all just
#     work.
#
