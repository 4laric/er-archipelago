# Names come from the WitchyBND FMG exports (msg/item-msgbnd-dcx + the item_dlc0{1,2}-msgbnd-dcx
# DLC name tables, *Name.fmg.xml); id == EquipParam/Goods/Gem id (verified Golden Rune [1]=2900 ->
