"""SPEC-PARITY Phase 7 -- Local Items convenience toggle (COMPLETE).

Greenfield's real vanilla items (ItemShuffle's weapons/armor/talismans/goods, plus the progressive
"Progressive X" upgrade items) are Elden-Ring-specific and mostly only useful *in* an ER game. In a
multiworld a player usually wants them to stay in their OWN world rather than scatter into other
games' pools, where they'd be dead weight. AP already supports this via the inherited `local_items`
OptionSet -- but that means the player has to list every item name by hand. This feature is a
one-flag convenience: flip it on and every real vanilla item (and every progressive item) is added
to `local_items`, so only the region Locks (progression) and Rune (generic filler) can appear in
other players' games.

MECHANISM (why mutating local_items in generate_early is both safe and effective):
  * `local_items` is AP's `LocalItems(ItemSet(OptionSet))`. `OptionSet.__init__` does
    `self.value = set(...)`, so `world.options.local_items.value` is a plain mutable `set[str]`.
    We mutate it IN PLACE (`.update(...)`), which is what AP itself does (Main.py subtracts sets in
    place). Rebinding `.value` would be fine too, but in-place update is the least surprising.
  * ORDERING. During YAML rolling, Generate.py calls `option.verify(...)` once, BEFORE any world
    hook. Because `LocalItems` is an `ItemSet` (`verify_item_name = True`), that verify checks that
    every listed name is a valid item name of this world -- but it runs on the *player-authored*
    value only, and never re-runs after generate_early. So adding names in generate_early cannot
    trip verify_item_name. (We only add names that ARE valid greenfield item names anyway --
    ITEM_CATALOG names + progressive names, all registered in core.item_name_to_id -- so even if a
    late re-verify were added upstream, our additions would pass.)
  * EFFECT. AP enforces locality in `Main.py` at `locality_rules(multiworld)` (called just before
    connect_entrances/generate_basic, i.e. long AFTER generate_early). It reads
    `world.options.local_items.value` fresh at that point and forbids every OTHER player's locations
    from holding those items. So our generate_early mutation is picked up. (locality_rules only runs
    when players > 1; in a solo game Main.py wipes local_items to the empty set -- a no-op, and the
    seed is trivially "all local" anyway. Our test asserts on options.local_items.value directly,
    which we populate regardless of player count.)

Matt-free: item names come from the param-derived ITEM_CATALOG and the progressive feature's own
constant names -- no curated/location data. slot_data: none (locality is enforced AP-server-side in
fill, the client never needs to know) -> {}.
"""
from typing import Any, Dict, List, Set

from Options import Toggle, OptionSet
from ..registry import Feature, register

try:  # generated by gen_data.py (real-item-pool: {vanilla_item_name: ER FullID}); matt-free
    from ..item_ids import ITEM_CATALOG
except Exception:  # not yet generated -> no real items exist -> feature is a no-op
    ITEM_CATALOG = {}

# Progressive "Progressive X" item names (registered by features/progressive.py, NOT in
# ITEM_CATALOG). Imported by NAME so this stays a pure data dependency -- importing the sibling
# feature module is fine (features may import other features; only ..core is forbidden). Guarded so
# a future removal of progressive.py degrades this to catalog-only rather than breaking import.
try:
    from .progressive import (PROG_FLASK, PROG_STONESWORD_KEY,
                              PROG_SMITHING_BELL, PROG_SOMBER_BELL)
    _PROGRESSIVE_NAMES: List[str] = [PROG_FLASK, PROG_STONESWORD_KEY,
                                     PROG_SMITHING_BELL, PROG_SOMBER_BELL]
except Exception:
    _PROGRESSIVE_NAMES = []

# ---- category buckets (ER FullID high nibble; matt-free -- pure arithmetic on the FullID) ------
# 0x0 = weapons, 0x1 = armor (protector), 0x2 = talismans (accessory), 0x4 = goods, 0x8 = ashes/gems.
_NIBBLE_CATEGORY: Dict[int, str] = {
    0x00000000: "weapons",
    0x10000000: "armor",
    0x20000000: "talismans",
    0x40000000: "goods",
    0x80000000: "ashes",
}
# All valid ExcludeLocalItemOnly keys (the categories a player may release back to foreign worlds),
# plus a "progressive" bucket for the Progressive X upgrade items.
_CATEGORY_KEYS: List[str] = sorted(set(_NIBBLE_CATEGORY.values()) | {"progressive"})


def _category_of(name: str) -> str:
    """Category key for a real vanilla item name, from its FullID high nibble. A name not in
    ITEM_CATALOG is a progressive item -> 'progressive'. Unknown nibble -> 'goods' (never silently
    dropped from the excludable set)."""
    full = ITEM_CATALOG.get(name)
    if full is None:
        return "progressive"
    return _NIBBLE_CATEGORY.get(full & 0xF0000000, "goods")


def _all_local_names() -> List[str]:
    """Every real vanilla item name (ITEM_CATALOG) + every progressive item name. These are exactly
    the items that only make sense in an ER game and that a player may want kept local."""
    return sorted(ITEM_CATALOG) + list(_PROGRESSIVE_NAMES)


class LocalItemOnly(Toggle):
    """Off (default). On: force every real vanilla item this seed produces -- all the shuffled
    weapons, armor, talismans, goods and ashes from Shuffle Vanilla Items, plus any Progressive
    upgrade items -- to stay in YOUR OWN world (adds them to Archipelago's local_items). Only the
    region Locks (progression) and Rune (generic filler) can then land in other players' games,
    which is usually what you want since ER items are only useful in an ER game. Has no effect when
    Shuffle Vanilla Items is off (there are no real items -- every check pays a Rune). This never
    changes winnability: Region Locks stay the sole progression and are unaffected."""
    display_name = "Local Items Only"


class ExcludeLocalItemOnly(OptionSet):
    """When Local Items Only is on, categories listed here are STILL allowed to travel to other
    players' worlds (i.e. NOT forced local). Use it to share some of your ER loot while keeping the
    rest. Valid categories: weapons, armor, talismans, goods, ashes, progressive. Empty (default) =
    keep everything local. Ignored unless Local Items Only is on."""
    display_name = "Exclude From Local Items Only"
    valid_keys = frozenset(_CATEGORY_KEYS)


@register
class LocalItemsFeature(Feature):
    name = "local_items"
    OPTIONS = {
        "local_item_only": LocalItemOnly,
        "exclude_local_item_only": ExcludeLocalItemOnly,
    }

    # ---- helpers ------------------------------------------------------------------------------
    def _shuffle_on(self, world) -> bool:
        sh = getattr(world.options, "item_shuffle", None)
        return bool(sh and sh.value)

    def _excluded_categories(self, world) -> Set[str]:
        ex = getattr(world.options, "exclude_local_item_only", None)
        # OptionSet.value is a set[str]; be tolerant if the option is somehow absent.
        return set(ex.value) if ex is not None else set()

    def names_to_localize(self, world) -> List[str]:
        """The concrete item names this feature will force local for `world`. Empty unless the
        toggle is on AND item_shuffle is on (real items only exist then). Names whose category is in
        exclude_local_item_only are dropped (left free to travel)."""
        toggle = getattr(world.options, "local_item_only", None)
        if not (toggle and toggle.value) or not self._shuffle_on(world):
            return []
        excluded = self._excluded_categories(world)
        if not excluded:
            return _all_local_names()
        return [nm for nm in _all_local_names() if _category_of(nm) not in excluded]

    # ---- hooks --------------------------------------------------------------------------------
    def generate_early(self, world) -> None:
        names = self.names_to_localize(world)
        if not names:
            return  # toggle off, or shuffle off (no real items) -> leave local_items untouched
        # local_items.value is a plain mutable set[str] (OptionSet.__init__ -> set(...)). Mutate it
        # in place; locality_rules reads it later in Main.py to forbid foreign locations. We only add
        # (never clear) so we compose with any names the player listed by hand.
        world.options.local_items.value.update(names)

    def slot_data(self, world) -> Dict[str, Any]:
        # Locality is enforced AP-server-side during fill (worlds/generic/Rules.locality_rules).
        # The client never needs to know, so no slot_data contract.
        return {}
