"""missable_locations -- keep required progression OUT of checks that can be permanently lost (matt-free).

Parity with the old apworld's `missable` location marker, but greenfield derives the set from its OWN
data (missable_locations.py, generated by gen_data): checks gated behind a LIMITED consumable or a
killable NPC, where a player might spend the resource elsewhere / kill the NPC and never obtain the
check. Currently:
  * Deathroot -- the Gurranq (Bestial Sanctum) reward checks (delivering deathroots; Gurranq killable).
  * Dragon Heart -- the Dragon Communion incantation purchases (ShopLineupParam costType==1).
(Stonesword-key / Shabriri / Seedbed deferred: questline-avoidance already keeps us clear of the latter.)

Enforcement is fill-side only (no slot_data key; the client is unaffected): each in-play missable
location gets an item-rule forbidding *advancement* (progression) items. Useful/filler are still allowed
-- a missable location may hold a nice-to-have you could miss, but never something required to win, so
the seed stays beatable even if the location is never reached. Default ON (parity + winnability).
"""
from Options import DefaultOnToggle
from ..registry import Feature, register

try:
    from ..missable_locations import MISSABLE_LOCATIONS
except Exception:  # not yet generated
    MISSABLE_LOCATIONS = {}


class ProtectMissableLocations(DefaultOnToggle):
    """Keep required progression out of missable checks (gated behind a limited consumable or a
    killable NPC: deathroot Gurranq rewards, Dragon-Heart / Dragon Communion purchases). On by
    default so the seed stays winnable even if those checks are never reached. Turn off to allow
    progression there (expert / guaranteed-access play)."""
    display_name = "Protect Missable Locations"


@register
class MissableLocationsFeature(Feature):
    name = "missable_locations"
    OPTIONS = {"protect_missable_locations": ProtectMissableLocations}

    def set_rules(self, world) -> None:
        if not world.options.protect_missable_locations.value or not MISSABLE_LOCATIONS:
            return
        tagged = [loc for loc in world.multiworld.get_locations(world.player)
                  if getattr(loc, "address", None) in MISSABLE_LOCATIONS]
        if not tagged:
            return
        # Fill-safety: forbidding advancement is only feasible if the pool has enough non-advancement
        # items to fill the tagged locations. This is essentially always true (filler alone dwarfs the
        # ~29 missable checks), but guard anyway so a degenerate pool degrades cleanly instead of churning.
        avail = sum(1 for i in world.multiworld.itempool
                    if i.player == world.player and not i.advancement)
        if avail < len(tagged):
            return
        for loc in tagged:
            prev = loc.item_rule
            loc.item_rule = lambda item, p=prev: p(item) and not item.advancement
